#include "libft.h"

int	ft_atoi(const char *str)
{
	int			i;
	long		res;
	int			sign;

	sign = 1;
	i = 0;
	res = 0;
	while (str[i] == ' ' || (str[i] >= '\t' && str[i] <= '\r'))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
		if (res > 2147483647 && sign == 1)
			return (0);
		if (res > 2147483648 && sign == -1)
			return (0);
	}
	return (int)(res * sign);
}
